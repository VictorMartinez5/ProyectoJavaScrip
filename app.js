//Escritura de productos

function mostrarProductos(productos) {
  const contenedor = document.querySelector("#contenedor");
  productos.forEach((prod) => {
    const { id, nombre, precio, img } = prod;
    if (contenedor) {
      contenedor.innerHTML +=
      `
      <div>
        <div class="porducto">
          <img src="${img}" alt="${nombre}">
          <div class="contenido_productos">
            <p class="title">${nombre}</p>
            <h4 class="precio">${precio}</h4>
            <button class="btn_add" onclick="agregarProducto(${id})">Añadir al Carrito</button>
          </div>
        </div>
      </div>
      `;
    }
  });
}

//Funcion agregado de productos al carrito

const agregarProducto = (id) => {
  const existe = carrito.some((prod) => prod.id === id);

  if (existe) {
    const index = carrito.findIndex((prod) => prod.id === id);
    carrito[index].cantidad++;
  } else {
    const item = productos.find((prod) => prod.id === id);
    carrito.push({ ...item, cantidad: 1 });
  }
  mostrarCarrito();
};


//Carrito 

const mostrarCarrito = () => {
  const modalBody = document.querySelector(".modal .modal-body");
  if (modalBody) {
    modalBody.innerHTML = "";
    carrito.forEach((prod) => {
      const { id, nombre, precio, img, cantidad } = prod;
      console.log(modalBody);
      modalBody.innerHTML += `
      <div class="modal-contenedor">
        <div>
        <img class="img-fluid img-carrito" src="${img}"/>
        </div>
        <div>
        <p>Producto: ${nombre}</p>
      <p>Precio: ${precio}</p>
      <p>Cantidad :${cantidad}</p>
      <button class="btn btn-danger"  onclick="eliminarProducto(${id})">Eliminar producto</button>
        </div>
      </div>
      
  
      `;
    });
  }

  if (carrito.length === 0) {
    console.log("Nada");
    modalBody.innerHTML = `
    <p class="text-center text-primary parrafo">¡El carrito esta vacio!</p>
    `;
  } else {
    console.log("Algo");
  }
  carritoContenedor.textContent = carrito.length;

  if (precioTotal) {
    precioTotal.innerText = carrito.reduce(
      (acc, prod) => acc + prod.cantidad * prod.precio,
      0
    );
  }

  guardarStorage();
};

//LocalStorage

function guardarStorage() {
  localStorage.setItem("carrito", JSON.stringify(carrito));
}

//Eliminado de productos

function eliminarProducto(id) {
  const remeraId = id;
  carrito = carrito.filter((remera) => remera.id !== remeraId);
  mostrarCarrito();
}

//Proceso del pedido

function procesarPedido() {
  carrito.forEach((prod) => {
    const listaCompra = document.querySelector("#lista-compra tbody");
    const { id, nombre, precio, img, cantidad } = prod;
    if (listaCompra) {
      const row = document.createElement("tr");
      row.innerHTML += `
              <td>
              <img class="img-fluid img-carrito" src="${img}"/>
              </td>
              <td>${nombre}</td>
            <td>${precio}</td>
            <td>${cantidad}</td>
            <td>${precio * cantidad}</td>
            `;
      listaCompra.appendChild(row);
    }
  });
  totalProceso.innerText = carrito.reduce(
    (acc, prod) => acc + prod.cantidad * prod.precio,
    0
  );
}



